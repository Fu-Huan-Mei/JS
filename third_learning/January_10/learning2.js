/*运算符（操作符）：对一个或多个操作数（值）进行运算*/
let a = 1 + 1;
//console.log(a);

/*
1、算术运算符：+ - * / **幂运算 %模运算（两个数相除取余数）
2、注意：算数运算时，除了字符串的加法，其他运算的操作是非数字时，都会转换为数值后在再运算  */
a = 2 * 4;
//console.log(a);
a =  4 ** 2;
//console.log(a);
a = 125 % 7;
// console.log(a);

/*JS是一门弱类型语言，进行运算时会通过自动的类型转换来完成运算*/
a = 10 - "5";//10-5
console.log(a);
//3、拼串：任意值和字符串做加法时，会先将其他值转为字符串，再拼串
